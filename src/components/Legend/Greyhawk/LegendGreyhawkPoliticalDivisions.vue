  <p>
    The buccaneers of the <legend-link>Azure Sea</legend-link> and
    <legend-link>Jeklea Bay</legend-link> grew strong and wealthy when
    <legend-link>Keoland</legend-link> was at the height of its power, for the eyes of its leaders
    were turned northwards towards empire, and the sea raiders were ignored. These privateers took
    to calling themselves <em>Sea Princes</em>, after a particularly successful captain who was in
    fact of noble birth. Sailing unchecked from their island and mainland strongholds, these raiders
    were the scourge of the coasts from <legend-link>Gradsul</legend-link> to
    <legend-link>Scant</legend-link>, on the <legend-link>Pomarj</legend-link>, and even beyond into
    the <legend-link>Sea of Gearnat</legend-link> and the <legend-link>Tilva Strait</legend-link>.
    When <legend-link>Keoland</legend-link> turned back from imperial expansion, her navy began to
    rebuild in order to check the threat of the Sea Princes — as they were now commonly known. Their
    numbers and strength had become so great, however, that the Keoish fleet, even with the aid of a
    squadron of <legend-link>Ulek</legend-link> warships, could at best deliver a sharp check to
    them (<em>Battle of <legend-link>Jetsom Island</legend-link></em
    >). This lesson caused their leaders to rethink their policies, however, and several of the
    wiser captains retired to mainland estates, appointing lieutenants to command their ships, not
    in piratical or raiding activities in the <em>Flanaess</em>, but on expeditions to the
    <legend-link>Amedio Coasts</legend-link> and thence to trade northwards with the rare woods,
    spices, ivory, and gold which they wrested from the jungle savages. Eventually the mainland
    possessions of the Sea Princes amounted to more territory than their island homes, and they
    practiced little formal raiding. Today they are still probably the strongest sea power, but they
    also have a small and efficient army and are relatively peaceful traders. If these people have a
    fault, it is that they allow the use of slaves in their nation, despite strong protests from the
    <legend-link>Yeomanry</legend-link>. It is reported that the Prince of
    <legend-link>Monmurg</legend-link> would abolish this practice, but his fellow nobles (the
    Prince of <legend-link>Toli</legend-link>, the Plar of <legend-link>Hool</legend-link>, and the
    Grandee of <legend-link>Westkeep</legend-link>, along with the Commodores of
    <legend-link>Jetsom</legend-link>, <legend-link>Fairwind</legend-link>, and
    <legend-link>Flotsom</legend-link>) prevent it.
  </p>

