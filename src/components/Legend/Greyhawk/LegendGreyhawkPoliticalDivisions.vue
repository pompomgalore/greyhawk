    The Ketite expedition came to grief in successive battles (<legend-link>Molvar</legend-link>,
    <legend-link>Lopolla</legend-link>), while an alliance between
    <legend-link>Veluna</legend-link>-<a href="#kingdom-furyondy">Furyondy</a> ended the Keoish
    threat in that quarter (<em>Short War</em>) Coincidentally, the Olvenfolk within the boundaries
    of Keoland objected to the warlike policies of the King and began expelling royal garrisons in
    the <legend-link>Ulek</legend-link> Provinces and <legend-link>Celene</legend-link>. In the
    ensuing struggle, the freemen of the western portion sided with the demi-humans. Raiders in the
    far south took advantage of these conditions to harry the Keoish coast from
    <legend-link>Gradsul</legend-link> to <legend-link>Gryrax</legend-link>.
  </p>
  <p>
    <em>King Tavish III</em> was slain in battle against the
    <legend-link>Sea Princes</legend-link> (Siege of <legend-link>Westkeep</legend-link>, 453 CY),
    and his son, <em>Tavish IV</em>, immediately changed the policies of the kingdom upon ascending
    to the throne. After protracted negotiations, the independent state of the
    <legend-link>Yeomanry</legend-link> was recognized, the Ulek states were granted autonomy, and
    Keoland returned to its former state of tolerance and prosperity thereafter. The
    semi-independent <legend-link>Gran March</legend-link> and Earldom of
    <legend-link>Sterich</legend-link> are loyal to the crown and furnish strong contingents to the
    royal army. The Keolanders are well-known for their light cavalry employing javelins, crossbows,
    and lances. The bulk of the army consists of foot armed with pole arms and long spears, while
    the nobility comprises the heavy cavalry portion of the force. There are typically small
    companies of elves, gnomes, halflings, and/or dwarves included in muster. The fleet is
    battleworthy but small, and conflict with the <legend-link>Sea Princes</legend-link> continues
    to plague the realm.
  </p>

